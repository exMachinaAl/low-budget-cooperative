    private void hidenButtonForDbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hidenButtonForDbMouseClicked
        // TODO add your handling code here:
        //change db
        String urlCustom = JOptionPane.showInputDialog(null, "masukkan url, ex: domainName/dbName", "custom-URL-DB", JOptionPane.WARNING_MESSAGE);
        try {
            if (!urlCustom.isEmpty()) {
                String userCustom = JOptionPane.showInputDialog(null, "userCustom DB", "custom-DB", JOptionPane.WARNING_MESSAGE);
                String passCustom = JOptionPane.showInputDialog(null, "passCustom DB", "custom-DB", JOptionPane.WARNING_MESSAGE);
                Mysql.setCustomDatabase(urlCustom, userCustom, passCustom);
                JOptionPane.showMessageDialog(null, "memulai ulang aplikasi");
                
                String java = System.getProperty("java.home") + "/bin/java";
                String classPath = System.getProperty("java.class.path");
                String className = KopRManager.class.getName();
                
                ProcessBuilder pcb = new ProcessBuilder(java, "-cp", classPath, className);
                pcb.inheritIO();
                pcb.start();
                
                this.dispose();
                System.exit(0);
            } else {
                System.out.println("cancel no argument");
            }
        } catch (Exception e) {
            System.out.println("cancel click");
        }
    }//GEN-LAST:event_hidenButtonForDbMouseClicked

