    public int mikuTalk(int mikuWannaSays) {
        switch (mikuWannaSays) {
            case 0 -> {
                mikuSays.setText("log o in to");
                mikuSays2.setText("");
                mikuSays3.setText("");
                mikuSays.setForeground(Color.BLACK);
                return mikuWannaSays + 1;
            }
            case 5 -> {
                mikuSays3.setText("jika anda");
                mikuSays2.setText("gk punya akun");
                mikuSays.setText("tekan miku");
                mikuSays.setForeground(Color.BLACK);
                mikuSays2.setForeground(Color.BLACK);
                mikuSays3.setForeground(Color.BLACK);
                return mikuWannaSays + 1;
            }
            default -> {
                if (mikuWannaSays == 10) {
                    return 0;
                }
                return mikuWannaSays + 1;
            }
        }
    }
    
